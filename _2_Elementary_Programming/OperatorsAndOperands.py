# The '/' operator is used to get the Floating point result of a certain division of number
# The '//' operator is used to get the Integer value when 2 numbers are divided.
# The '**' operator is used to denote exponentiation (raise to the power of)

print(4 / 2)    # This results in a floating point value.
print(4 // 2)   # This results in an integer value.
print(20 % 3)   # The remainder when we divide 20 by 3.
print(6 ** 3)   # Returns 6 * 6 * 6





